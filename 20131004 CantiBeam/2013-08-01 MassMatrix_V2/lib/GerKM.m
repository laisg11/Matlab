function [K,M]=GerKM(ds,E,v,dsy)
S12=20;
S34=20;
d=100;
h=40;
% Nodes
Nodes=[0   -S12/2   h;...
       0    S12/2   h;...
       d    S34/2   h;...
       d   -S34/2   h;...
       0   -S12/2   0;...
       0    S12/2   0;...
       d    S34/2   0;...
       d   -S34/2   0;];
R(8,8)=0;
for i=1:8
    R(i,:)=[1 Nodes(i,1) Nodes(i,2) Nodes(i,3) Nodes(i,1)*Nodes(i,2) ...
        Nodes(i,2)*Nodes(i,3) Nodes(i,1)*Nodes(i,3) ...
        Nodes(i,1)*Nodes(i,2)*Nodes(i,3)];
end
N=inv(R);

f1=E*(1-v)/(1+v)/(1-2*v);
f2=f1*(1-2*v)/2/(1-v);
f3=E*v/(1+v)/(1-2*v);
D=       [f1    f3     f3     0      0      0; ...
          f3    f1     f3     0      0      0; ...
          f3    f3     f1     0      0      0; ...
          0     0      0      f2     0      0; ...
          0     0      0      0      f2     0; ...
          0     0      0      0      0      f2;];
      
[K,M]=CalKM(ds,dsy,N,D);
end